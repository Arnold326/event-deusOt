        <script>

            function openForm() {
                document.getElementById("formOverlay").style.height = "100%";
            }


            function closeForm() {
                document.getElementById("formOverlay").style.height = "0";
            }

            window.onclick = function(event) {
                if (event.target === document.getElementById("formOverlay")) {
                    closeForm();
                }
            }

            let currentStep = 1;

            function showStep(step) {
                const steps = document.querySelectorAll('.form-step');
                steps.forEach((stepDiv) => stepDiv.classList.remove('active'));
                document.getElementById(`step-${step}`).classList.add('active');
            }

            function nextStep() {
                if (validateStep(currentStep)) {
                    currentStep++;
                    showStep(currentStep);
                }
            }

            function prevStep() {
                currentStep--;
                showStep(currentStep);
            }

            function validateStep(step) {
                const currentFields = document.querySelectorAll(`#step-${step} input[required], #step-${step} select[required]`);
                let isValid = true;

                currentFields.forEach((field) => {
                    const errorMessage = field.parentNode.querySelector('.error-message');
                    if (!field.value) {
                        field.classList.add('error');
                        if (!errorMessage) {
                            const errorElement = document.createElement('span');
                            errorElement.classList.add('error-message');
                            errorElement.textContent = `Este campo é obrigatório.`;
                            field.parentNode.appendChild(errorElement);
                        }
                        isValid = false;
                    } else {
                        field.classList.remove('error');
                        if (errorMessage) {
                            errorMessage.remove();
                        }
                    }
                });

                return isValid;
            }

            showStep(currentStep);

            // Validação final ao enviar
            document.getElementById('registrationForm').addEventListener('submit', function(event) {
                if (!validateStep(currentStep)) {
                    event.preventDefault(); // Impede o envio se a etapa atual não for válida
                }
            });



            function countdown() {
                const eventDate = new Date("December 20, 2024 00:00:00").getTime();
                const now = new Date().getTime();
                const distance = eventDate - now;

                const dias = Math.floor(distance / (1000 * 60 * 60 * 24));
                const horas = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
                const minutos = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
                const segundos = Math.floor((distance % (1000 * 60)) / 1000);

                document.getElementById('dias').innerText = dias;
                document.getElementById('horas').innerText = horas;
                document.getElementById('minutos').innerText = minutos;
                document.getElementById('segundos').innerText = segundos;

                if (distance < 0) {
                    clearInterval(countdownInterval);
                    document.getElementById('contador').innerHTML = "Evento já começou!";
                }
            }

            const countdownInterval = setInterval(countdown, 1000);

            AOS.init({
                duration: 1200, // Duração da animação em milissegundos
            });
        </script>


